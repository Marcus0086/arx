    // ── PackStream ───────────────────────────────────────────────────────────
    async fn pack_stream(
        &self,
        request: Request<Streaming<UploadFrame>>,
    ) -> Result<Response<PackResponse>, Status> {
        let tenant_id = {
            let _t = extract_bearer(&request)?;
            extract_tenant(&_t, &self.db).await?
        };
        let mut stream = request.into_inner();

        // First frame must be PackHeader
        let first = stream
            .message()
            .await?
            .ok_or_else(|| Status::invalid_argument("empty upload stream"))?;
        let header = match first.payload {
            Some(upload_frame::Payload::PackHeader(h)) => h,
            _ => return Err(Status::invalid_argument("first frame must be PackHeader")),
        };

        let archive_id = self
            .store
            .new_archive(&tenant_id, &header.archive_name)
            .map_err(|e| Status::internal(e.to_string()))?;
        let paths = self.store.archive_paths(&tenant_id, &archive_id);

        // Write incoming files to a tempdir, then pack
        let tmp = tempfile::tempdir().map_err(|e| Status::internal(e.to_string()))?;
        let mut current_file: Option<(std::path::PathBuf, std::fs::File)> = None;

        while let Some(frame) = stream.message().await? {
            match frame.payload {
                Some(upload_frame::Payload::FileInfo(fi)) => {
                    if let Some((_, mut f)) = current_file.take() {
                        let _ = std::io::Write::flush(&mut f);
                    }
                    let dest = tmp.path().join(fi.path.trim_start_matches('/'));
                    if let Some(parent) = dest.parent() {
                        std::fs::create_dir_all(parent)
                            .map_err(|e| Status::internal(e.to_string()))?;
                    }
                    let f = std::fs::File::create(&dest)
                        .map_err(|e| Status::internal(e.to_string()))?;
                    current_file = Some((dest, f));
                }
                Some(upload_frame::Payload::Data(bytes)) => {
                    if let Some((_, ref mut f)) = current_file {
                        use std::io::Write;
                        f.write_all(&bytes)
                            .map_err(|e| Status::internal(e.to_string()))?;
                    }
                }
                Some(upload_frame::Payload::Finalize(_)) => break,
                _ => {}
            }
        }
        drop(current_file);

        let key = header.key.as_ref();
        let aead_key = resolve_key_no_archive(key)?;
        let password = password_from_key(key);

        let opts = PackOptions {
            deterministic: header.deterministic,
            min_gain: if header.min_gain > 0.0 {
                header.min_gain
            } else {
                0.05
            },
            aead_key,
            password,
            meta_label: if header.label.is_empty() {
                None
            } else {
                Some(header.label)
            },
            meta_owner: if header.owner.is_empty() {
                None
            } else {
                Some(header.owner)
            },
            meta_notes: if header.notes.is_empty() {
                None
            } else {
                Some(header.notes)
            },
            ..Default::default()
        };

        let input_path = tmp.path().to_path_buf();

        tokio::task::spawn_blocking({
            let out = paths.data.clone();
            move || {
                let refs = vec![input_path.as_path()];
                pack(&refs, &out, Some(&opts))
            }
        })
        .await
        .map_err(|e| Status::internal(e.to_string()))?
        .map_err(arx_err)?;

        Ok(Response::new(PackResponse {
            archive_id,
            stats: None,
            error: String::new(),
        }))
    }

    // ── List ─────────────────────────────────────────────────────────────────
    async fn list(&self, request: Request<ListRequest>) -> Result<Response<ListResponse>, Status> {
        let tenant_id = {
            let _t = extract_bearer(&request)?;
            extract_tenant(&_t, &self.db).await?
        };
        let req = request.into_inner();
        let archive_path = self.store.resolve(&tenant_id, &req.archive_id)?;
        let aead_key = resolve_key(&archive_path, req.key.as_ref())?;
        let opts = aead_key.map(|k| ListOptions {
            aead_key: Some(k),
            key_salt: [0u8; 32],
        });

        // Use the repo to get entries
        let params = OpenParams {
            archive_path: archive_path.clone(),
            aead_key: opts.as_ref().and_then(|o| o.aead_key),
            key_salt: [0u8; 32],
        };
        let repo = open_repo(Backend::Fs, params).map_err(arx_err)?;
        let files = repo.list_files().map_err(arx_err)?;

        let entries = files
            .into_iter()
            .map(|f| ListEntry {
                path: f.path,
                u_size: f.u_size,
                c_size: 0, // TODO: sum c_sizes from chunk_map
                chunks: f.chunks as u32,
            })
            .collect();

        Ok(Response::new(ListResponse { entries }))
    }

    // ── ExtractStream ────────────────────────────────────────────────────────
    type ExtractStreamStream = ReceiverStream<Result<DownloadFrame, Status>>;

    async fn extract_stream(
        &self,
        request: Request<ExtractRequest>,
    ) -> Result<Response<Self::ExtractStreamStream>, Status> {
        let tenant_id = {
            let _t = extract_bearer(&request)?;
            extract_tenant(&_t, &self.db).await?
        };
        let req = request.into_inner();
        let archive_path = self.store.resolve(&tenant_id, &req.archive_id)?;
        let aead_key = resolve_key(&archive_path, req.key.as_ref())?;

        let (tx, rx) = mpsc::channel(16);

        tokio::task::spawn_blocking(move || {
            let params = OpenParams {
                archive_path: archive_path.clone(),
                aead_key,
                key_salt: [0u8; 32],
            };
            let repo = match open_repo(Backend::Fs, params) {
                Ok(r) => r,
                Err(e) => {
                    let _ = tx.blocking_send(Err(arx_err(e)));
                    return;
                }
            };

            let files = match repo.list_files() {
                Ok(f) => f,
                Err(e) => {
                    let _ = tx.blocking_send(Err(arx_err(e)));
                    return;
                }
            };

            for file in files {
                if !req.path.is_empty() && file.path != req.path {
                    continue;
                }
                // Send file info frame
                let _ = tx.blocking_send(Ok(DownloadFrame {
                    payload: Some(download_frame::Payload::File(FileInfo {
                        path: file.path.clone(),
                        mode: 0o644,
                        mtime: 0,
                        size: file.u_size,
                    })),
                }));
                // Stream content
                let start = if req.path.is_empty() { 0 } else { req.start };
                let len = if req.len == 0 { u64::MAX / 4 } else { req.len };
                let mut reader = match repo.open_range(&file.path, start, len) {
                    Ok(r) => r,
                    Err(e) => {
                        let _ = tx.blocking_send(Err(arx_err(e)));
                        return;
                    }
                };
                let mut buf = vec![0u8; 512 * 1024];
                loop {
                    use std::io::Read;
                    let n = match reader.read(&mut buf) {
                        Ok(n) => n,
                        Err(e) => {
                            let _ = tx.blocking_send(Err(Status::internal(e.to_string())));
                            return;
                        }
                    };
                    if n == 0 {
                        break;
                    }
                    let _ = tx.blocking_send(Ok(DownloadFrame {
                        payload: Some(download_frame::Payload::Data(buf[..n].to_vec())),
                    }));
                }
            }
        });

        Ok(Response::new(ReceiverStream::new(rx)))
    }

