impl ArxServiceImpl {
    fn archive_lock(&self, tenant_id: &str, archive_id: &str) -> Arc<Mutex<()>> {
        let key = format!("{tenant_id}:{archive_id}");
        self.archive_locks
            .entry(key)
            .or_insert_with(|| Arc::new(Mutex::new(())))
            .clone()
    }
}

/// Map ArxError to tonic Status.
fn arx_err(e: ArxError) -> Status {
    match e {
        ArxError::AeadError => Status::unauthenticated("AEAD authentication failed — wrong key"),
        ArxError::Io(_) => Status::internal("storage error"),
        ArxError::Format(msg) => Status::invalid_argument(msg),
    }
}

/// Resolve a raw 32-byte key from KeyMaterial + archive superblock (for password derivation).
fn resolve_key(archive_path: &Path, key: Option<&KeyMaterial>) -> Result<Option<[u8; 32]>, Status> {
    let Some(km) = key else { return Ok(None) };
    match &km.key_source {
        Some(key_material::KeySource::RawKey(bytes)) => {
            if bytes.len() != 32 {
                return Err(Status::invalid_argument("raw_key must be exactly 32 bytes"));
            }
            let mut arr = [0u8; 32];
            arr.copy_from_slice(bytes);
            Ok(Some(arr))
        }
        Some(key_material::KeySource::Password(pw)) => {
            // Read kdf_salt from the archive's superblock
            let mut f =
                std::fs::File::open(archive_path).map_err(|e| Status::internal(e.to_string()))?;
            let sb = Superblock::read_from(&mut f).map_err(|e| Status::internal(e.to_string()))?;
            Ok(Some(derive_key(pw, &sb.kdf_salt)))
        }
        None => Ok(None),
    }
}

/// Resolve a raw key without needing an archive path (for pack / issue where archive doesn't exist yet).
fn resolve_key_no_archive(key: Option<&KeyMaterial>) -> Result<Option<[u8; 32]>, Status> {
    let Some(km) = key else { return Ok(None) };
    match &km.key_source {
        Some(key_material::KeySource::RawKey(bytes)) => {
            if bytes.len() != 32 {
                return Err(Status::invalid_argument("raw_key must be exactly 32 bytes"));
            }
            let mut arr = [0u8; 32];
            arr.copy_from_slice(bytes);
            Ok(Some(arr))
        }
        Some(key_material::KeySource::Password(pw)) => {
            // For new archives, use password directly — kdf_salt is auto-generated by pack()
            // We store the password in PackOptions.password
            let _ = pw; // handled in PackOptions below
            Ok(None) // password passed through PackOptions.password
        }
        None => Ok(None),
    }
}

fn password_from_key(key: Option<&KeyMaterial>) -> Option<String> {
    key.and_then(|km| match &km.key_source {
        Some(key_material::KeySource::Password(pw)) => Some(pw.clone()),
        _ => None,
    })
}

